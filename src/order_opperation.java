public class order_opperation {
    public static void main(String[] args) {
        //operators
        int a = 5;
        System.out.println("pre increment : " + (++a) );
        System.out.println("pre decement : " + (--a));
        System.out.println("post increment : " + (a++));
        System.out.println("pre decement : " + (a--));
        System.out.println(a);
    }
}
